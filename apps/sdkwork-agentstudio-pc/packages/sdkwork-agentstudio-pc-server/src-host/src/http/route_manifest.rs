//! SDKWork HTTP route manifest — declares all business routes for OpenAPI
//! materialization, auth-mode resolution, and contract fallback per
//! `WEB_FRAMEWORK_STANDARD.md` §3–§4.
//!
//! Each `HttpRoute` entry carries the method, path template, tag,
//! operation id, and auth mode. The framework's 18-stage interceptor
//! pipeline uses this manifest to:
//! - Resolve `RouteAuth::Public` vs protected routes (§3.4)
//! - Materialize OpenAPI operations with `x-sdkwork-route-auth` (§3.3 I6)
//! - Drive contract-fallback 404/405 responses (§4)

