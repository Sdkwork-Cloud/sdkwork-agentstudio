/// Build the `HttpRouteManifest` from the static route list.
pub fn claw_server_route_manifest() -> HttpRouteManifest {
    HttpRouteManifest::new(CLAW_SERVER_ROUTES)
}

/// Infer the API surface for a given path (used by the framework pipeline).
#[allow(dead_code)]
pub fn infer_surface(path: &str) -> ApiSurface {
    sdkwork_web_contract::infer_api_surface_from_path(path)
}
